    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        if(networkJComboBox.getItemCount()==0){
            JOptionPane.showMessageDialog(null, "Please create the network first");
            return;
        }
        
        
        Network network = (Network) networkJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        if(nameTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please input the name");
            return;
        }
        
        String name = nameTxt.getText();
        for(Network net: system.getNetworkList()){
            for(Enterprise enter: net.getEnterpriseDirectory().getEnterpriseList()){
                if(enter.getName().equalsIgnoreCase(name)){
                    JOptionPane.showMessageDialog(null, "Name has existed. Please input the another name for enterprise");
                    return;
                }
            }
        }
        

        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);

        populateTable();
    }//GEN-LAST:event_submitBtnActionPerformed

