    public void populateOrder(){
        DefaultTableModel model = (DefaultTableModel) orderjTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            FinanceTestWorkRequest r = (FinanceTestWorkRequest)request;
            //if (r.getPrepareStatus().equals("Waiting")){
                Object[] row = new Object[4];
                row[0] = r;
                row[1] = r.getMaterial().getProductionModel();
                row[2] = r.getQuantity();
                row[3] = r.getPrepareStatus();
                
                model.addRow(row);
            //}
        }
    }
    
    public void popluateInventoryjTable(){
        DefaultTableModel dtm = (DefaultTableModel)inventoryjTable.getModel();
        dtm.setRowCount(0);
        
        for(Storage a : organization.getInventory()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getQuantity();
            row[2]=a.getMaterial().getUnitPrice();
            dtm.addRow(row);
        }
    }

