    private void checkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = orderjTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select any row");
            return;
        }
        
        FinanceTestWorkRequest request = (FinanceTestWorkRequest)orderjTable.getValueAt(selectedRow, 0);
        //check validation
        if (!request.getPrepareStatus().equals("Waiting")){
            JOptionPane.showMessageDialog(null, "This order have already Finished");
            return;
        }
        
        boolean flag = false;
        for (Storage s : organization.getInventory()){
            if (request.getMaterial().getProductionModel().equals(s.getMaterial().getProductionModel())){
                flag = true;
                if (request.getQuantity() <= s.getQuantity()){
                    request.setPrepareStatus("Ready to Send");
                    request.getMaterial().setUnitPrice(s.getMaterial().getUnitPrice());
                    s.setQuantity(s.getQuantity()-request.getQuantity());
                }
                else {
                    request.setPrepareStatus("Insufficient Goods");
                }
                break;
            }
        }
        
        if(!flag){
            request.setPrepareStatus("Insufficient Goods");
        }
        
        populateOrder();
        popluateInventoryjTable();
    }//GEN-LAST:event_checkBtnActionPerformed


